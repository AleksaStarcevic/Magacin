package artikli;


public class Artikal {
	private int sifra;
	private	String naziv;
	private String opis;
	private int kol;
	
	public int getSifra() {
		return sifra;
	}
	
	public String getNaziv() {
		return naziv;
	}

	public void setNaziv(String naziv) {
		this.naziv = naziv;
	}

	public String getOpis() {
		return opis;
	}

	public void setOpis(String opis) {
		this.opis = opis;
	}

	public int getKol() {
		return kol;
	}

	public void setKol(int kol) {
		this.kol = kol;
	}

	public void setSifra(int sifra) {
		this.sifra = sifra;
	}

	

	
}
